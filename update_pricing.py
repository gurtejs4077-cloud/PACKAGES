# Find the structure and rebuild each page
import re

for filename, d in pages.items():
    content = open(filename).read()

    # Replace niche name in title and subtitle
    content = re.sub(r'tailored for .+?\.', f'tailored for {d["niche_name"]}.', content)
    content = re.sub(r'<title>.+?</title>', f'<title>{d["niche_name"]} - Pricing Packages</title>', content)

    # Basic card
    content = re.sub(r'Basic &quot;.+?&quot;', f'Basic &quot;{d["b_name"]}&quot;', content)
    content = re.sub(r'Basic ".+?"', f'Basic "{d["b_name"]}"', content)
    content = re.sub(r'(__B_PRICE__|' + re.escape(next((v["b_price"] for k,v in pages.items() if k==filename), "")) + r')', d["b_price"], content)

    # Pro card  
    content = re.sub(r'Pro &quot;.+?&quot;', f'Pro &quot;{d["p_name"]}&quot;', content)
    content = re.sub(r'Pro ".+?"', f'Pro "{d["p_name"]}"', content)

    # Premium card
    content = re.sub(r'Premium &quot;.+?&quot;', f'Premium &quot;{d["pr_name"]}&quot;', content)
    content = re.sub(r'Premium ".+?"', f'Premium "{d["pr_name"]}"', content)

    with open(filename, 'w') as f:
        f.write(content)

print("Done patching names")
